package net.blay09.mods.chattweaks.chat.emotes.ffz;

public class FFZEmoteData {
    private final String id;
    private final String url;

    public FFZEmoteData(String id, String url) {
        this.id = id;
        this.url = url;
    }

    public String getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }
}
